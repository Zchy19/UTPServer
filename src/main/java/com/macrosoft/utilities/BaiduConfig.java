package com.macrosoft.utilities;

public class BaiduConfig {
	
	private String ocrAppId;
	private String ocrApiKey;
	private String ocrSecretKey;
	private String qrAppId;
	private String qrApiKey;
	private String qrSecretKey;	
	
	public String getOcrAppId() {
		return ocrAppId;
	}
	public void setOcrAppId(String ocrAppId) {
		this.ocrAppId = ocrAppId;
	}
	public String getOcrApiKey() {
		return ocrApiKey;
	}
	public void setOcrApiKey(String ocrApiKey) {
		this.ocrApiKey = ocrApiKey;
	}
	public String getOcrSecretKey() {
		return ocrSecretKey;
	}
	public void setOcrSecretKey(String ocrSecretKey) {
		this.ocrSecretKey = ocrSecretKey;
	}
	public String getQrAppId() {
		return qrAppId;
	}
	public void setQrAppId(String qrAppId) {
		this.qrAppId = qrAppId;
	}
	public String getQrApiKey() {
		return qrApiKey;
	}
	public void setQrApiKey(String qrApiKey) {
		this.qrApiKey = qrApiKey;
	}
	public String getQrSecretKey() {
		return qrSecretKey;
	}
	public void setQrSecretKey(String qrSecretKey) {
		this.qrSecretKey = qrSecretKey;
	}	
}
